#include <iostream>
using namespace std;

int main4() {

	//=
	int a = 10;
	a = 100;
	cout << "a = " << a << endl;

	// +=
	a = 10;
	a += 2; 
	cout << "a = " << a << endl;

	//-=
	a = 10;
	a -= 2;
	cout << "a = " << a << endl;

	//*=
	a = 10;
	a *= 2;
	cout << "a = " << a << endl;

	// /=
	a = 10;
	a /= 2;
	cout << "a = " << a << endl;

	// %=
	a = 10;
	a %= 2;
	cout << "a = " << a << endl;






	system("pause");

	return 0;
}